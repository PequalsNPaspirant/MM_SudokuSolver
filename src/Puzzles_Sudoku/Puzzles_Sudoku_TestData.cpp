	SudokuPuzzleData::SudokuPuzzleData(const vector< vector<int> >& dataIn, const vector< vector<int> >& solution /*= vector< vector<int> >(0, vector<int>(0))*/, const string& category /*= ""*/)
		: m_sudokuPuzzleData(std::move(dataIn)), m_solution(std::move(solution)), m_category(category)
	{
	}

	// Obsolete constructors used only for hardcoded sets
	SudokuPuzzleData::SudokuPuzzleData(const string& str, const string& solution /*= ""*/, const size_t dim /*= 9*/)
		: m_sudokuPuzzleData(vector< vector<int> >(0, vector<int>(0, 0))),
		m_solution(vector< vector<int> >(0, vector<int>(0)))
	{
		size_t separator = str.find_first_of('/');
		string puzzleString(str);
		if (separator != std::string::npos)
		{
			puzzleString = str.substr(0, separator - 1);
			m_category = str.substr(separator);
		}

		m_sudokuPuzzleData = SudokuPuzzleUtils::convertToVectorOfVector(puzzleString, dim);

		if (!solution.empty())
			m_solution = SudokuPuzzleUtils::convertToVectorOfVector(solution, dim);
	}

	const vector< vector<int> >& SudokuPuzzleData::getPuzzleData() const
	{
		return m_sudokuPuzzleData;
	}

	const vector< vector<int> >& SudokuPuzzleData::getSolution() const
	{
		return m_solution;
	}

	const string& SudokuPuzzleData::getCategory() const
	{
		return m_category;
	}

